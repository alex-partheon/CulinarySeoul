function SidebarContent({ navigation }: { navigation: any[] }) {
  const location = useLocation()
  const [expandedItems, setExpandedItems] = useState<string[]>([])
  
  const toggleExpanded = (itemName: string) => {
    setExpandedItems(prev => 
      prev.includes(itemName) 
        ? prev.filter(name => name !== itemName)
        : [...prev, itemName]
    )
  }
  
  return (
    <div className="flex-1 flex flex-col min-h-0 bg-white border-r border-gray-200">
      <div className="flex-1 flex flex-col pt-5 pb-4 overflow-y-auto">
        <div className="flex items-center flex-shrink-0 px-4">
          <h2 className="text-lg font-semibold text-gray-900">CulinarySeoul</h2>
        </div>
        <nav className="mt-5 flex-1 px-2 space-y-1">
          {navigation.map((item) => {
            const isActive = location.pathname === item.href || 
                           (item.subMenu && item.subMenu.some(sub => location.pathname === sub.href))
            const isExpanded = expandedItems.includes(item.name)
            
            if (item.subMenu) {
              return (
                <div key={item.name}>
                  <button
                    onClick={() => toggleExpanded(item.name)}
                    className={cn(
                      isActive
                        ? 'bg-indigo-100 text-indigo-900'
                        : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900',
                      'group flex items-center justify-between w-full px-2 py-2 text-sm font-medium rounded-md'
                    )}
                  >
                    <div className="flex items-center">
                      <div className={cn(
                        isActive ? 'text-indigo-500' : 'text-gray-400 group-hover:text-gray-500',
                        'mr-3 flex-shrink-0'
                      )}>
                        {item.icon}
                      </div>
                      {item.name}
                    </div>
                    <svg
                      className={cn(
                        'w-4 h-4 transition-transform',
                        isExpanded ? 'transform rotate-180' : ''
                      )}
                      fill="none"
                      stroke="currentColor"
                      viewBox="0 0 24 24"
                    >
                      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                    </svg>
                  </button>
                  {isExpanded && (
                    <div className="ml-8 mt-1 space-y-1">
                      {item.subMenu.map((subItem) => {
                        const isSubActive = location.pathname === subItem.href
                        return (
                          <Link
                            key={subItem.name}
                            to={subItem.href}
                            className={cn(
                              isSubActive
                                ? 'bg-indigo-50 text-indigo-700'
                                : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900',
                              'block px-2 py-2 text-sm rounded-md'
                            )}
                          >
                            {subItem.name}
                          </Link>
                        )
                      })}
                    </div>
                  )}
                </div>
              )
            }
            
            return (
              <Link
                key={item.name}
                to={item.href}
                className={cn(
                  isActive
                    ? 'bg-indigo-100 text-indigo-900'
                    : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900',
                  'group flex items-center px-2 py-2 text-sm font-medium rounded-md'
                )}
              >
                <div className={cn(
                  isActive ? 'text-indigo-500' : 'text-gray-400 group-hover:text-gray-500',
                  'mr-3 flex-shrink-0'
                )}>
                  {item.icon}
                </div>
                {item.name}
              </Link>
            )
          })}
        </nav>
      </div>
    </div>
  )
}